pub fn modifiers_to_godot_key(
    modifiers: Modifiers,
) -> impl Iterator<Item = (GodotKey, KeyLocation)> {
    modifiers.into_iter().filter_map(|m| {
        Some(match m {
            Modifiers::LCTRL => (GodotKey::CTRL, KeyLocation::LEFT),
            Modifiers::LSHIFT => (GodotKey::SHIFT, KeyLocation::LEFT),
            Modifiers::LALT => (GodotKey::ALT, KeyLocation::LEFT),
            Modifiers::LSUPER => (GodotKey::META, KeyLocation::LEFT),
            Modifiers::RCTRL => (GodotKey::CTRL, KeyLocation::RIGHT),
            Modifiers::RSHIFT => (GodotKey::SHIFT, KeyLocation::RIGHT),
            Modifiers::RALT => (GodotKey::ALT, KeyLocation::RIGHT),
            Modifiers::RSUPER => (GodotKey::META, KeyLocation::RIGHT),
            _ => return None,
        })
    })
}
